#include "3-calc.h"
/**
 * op_add - adds 2 integers
 * @a: 1st
 * @b: 2nd
 * Return: sum.
 */
int op_sum(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - adds 2 integers
 * @a: 1st
 * @b: 2nd
 * Return: sum.
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies 2 integers
 * @a: 1st
 * @b: 2nd
 * Return: sum.
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides 2 integers
 * @a: 1st
 * @b: 2nd
 * Return: sum.
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - finds the remainder 2 integers
 * @a: 1st
 * @b: 2nd
 * Return: sum.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
